namespace MineJason.SNbt;

/// <summary>
/// Defines constants related with string NBT.
/// </summary>
public static class SNbtConstants
{
    /// <summary>
    /// The left square bracket.
    /// </summary>
    public const char LeftSquareBracket = '[';
    /// <summary>
    /// The right square bracket.
    /// </summary>
    public const char RightSquareBracket = ']';
    /// <summary>
    /// The left curve bracket.
    /// </summary>
    public const char LeftCurveBracket = '{';
    /// <summary>
    /// The right curve bracket.
    /// </summary>
    public const char RightCurveBracket = '}';
    /// <summary>
    /// A comma.
    /// </summary>
    public const char Comma = ',';
    /// <summary>
    /// The colon.
    /// </summary>
    public const char Colon = ':';
    /// <summary>
    /// The double quote sign.
    /// </summary>
    public const char DoubleQuote = '"';
    /// <summary>
    /// The single quote sign.
    /// </summary>
    public const char SingleQuote = '\'';
}
